package com.uni.core.dto.etc;

public class TimeDTO {
	
	private int year;
	
	private boolean firstPartYear;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public boolean isFirstPartYear() {
		return firstPartYear;
	}

	public void setFirstPartYear(boolean firstPartYear) {
		this.firstPartYear = firstPartYear;
	}
	
}
